package com.pordiversion.bean;

public class Juego {

	private String idJuego;
	private String mensaje;
	private String minuto;
	private String local;
	private String visitante;
	private String tiempoJuego;
	private String marcador;
	
	/**
	 * @return the marcador
	 */
	public String getMarcador() {
		return marcador;
	}
	/**
	 * @param marcador the marcador to set
	 */
	public void setMarcador(String marcador) {
		this.marcador = marcador;
	}
	/**
	 * @return the tiempoJuego
	 */
	public String getTiempoJuego() {
		return tiempoJuego;
	}
	/**
	 * @param tiempoJuego the tiempoJuego to set
	 */
	public void setTiempoJuego(String tiempoJuego) {
		this.tiempoJuego = tiempoJuego;
	}
	/**
	 * @return the idJuego
	 */
	public String getIdJuego() {
		return idJuego;
	}
	/**
	 * @param idJuego the idJuego to set
	 */
	public void setIdJuego(String idJuego) {
		this.idJuego = idJuego;
	}
	/**
	 * @return the mensaje
	 */
	public String getMensaje() {
		return mensaje;
	}
	/**
	 * @param mensaje the mensaje to set
	 */
	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}
	/**
	 * @return the minuto
	 */
	public String getMinuto() {
		return minuto;
	}
	/**
	 * @param minuto the minuto to set
	 */
	public void setMinuto(String minuto) {
		this.minuto = minuto;
	}
	/**
	 * @return the local
	 */
	public String getLocal() {
		return local;
	}
	/**
	 * @param local the local to set
	 */
	public void setLocal(String local) {
		this.local = local;
	}
	/**
	 * @return the visitante
	 */
	public String getVisitante() {
		return visitante;
	}
	/**
	 * @param visitante the visitante to set
	 */
	public void setVisitante(String visitante) {
		this.visitante = visitante;
	}

	
}
